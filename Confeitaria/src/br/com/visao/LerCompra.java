    private void jBConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConsultaActionPerformed
        // TODO add your handling code here:
        try {
        // Inst√¢ncia do DAO de compras para realizar a consulta
        DAOCompra daoCompra = new DAOCompra();
        List<Object[]> compras = daoCompra.lerCompra(); // M√©todo que retorna as compras

        // Obtem o modelo da tabela
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpa a tabela antes de adicionar os novos dados

        // Preenche a tabela com os dados da consulta
        for (Object[] compra : compras) {
            model.addRow(compra);
        }

        JOptionPane.showMessageDialog(this, "Consulta realizada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao consultar compras: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBConsultaActionPerformed

